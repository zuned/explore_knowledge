package com.zuni.reporting.core;

public class HCReportingException extends Exception{

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	public HCReportingException() {
		super();
		
	}

	public HCReportingException(String message, Throwable cause) {
		super(message, cause);
		
	}

	public HCReportingException(String message) {
		super(message);
		
	}

	public HCReportingException(Throwable cause) {
		super(cause);
		
	}
	
}
